// with typed parameters
function sum(num1, num2) {
    return num1 + num2;
}
var value = sum(5, 3);
console.log("result-------->" + value);
//-----------------------------------------------------
// return string 
function sum1(num1, num2) {
    return num1 + num2;
}
var value1 = sum1('5', 3);
console.log("result1-------->" + value1);
//-----------------------------------------------------
// 'any' type of parameters
function sum2(num1, num2) {
    return num1 + num2;
}
var value2 = sum2(5, 3);
console.log("result2-------->" + value2);
